
public class Person {
	
	private String name;
	private String PhoneNo;
	
	public Person(String _name, String _phoneNo) {
		this.name = _name;
		this.PhoneNo = _phoneNo;
	}
	
	public String getName() {
		return this.name;
	}
	
	public String getPhoneNo() {
		return this.PhoneNo;
	}

}
